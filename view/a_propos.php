      <h3>Le projet</h3>
      <p>Ce site a été réalisé dans le cadre du cours d'Interface Homme-Machine (IGR 203) de Telecom ParisTech. Le but était après avoir trouvé une idée de projet viable,
      de définir les besoins des utilisateurs et de les traduire dans une application fonctionnelle. Etant donné les fonctionnalités offertes par cette application nous avons immédiatement
      opté pour un site WEB.</p>

      <h3>Origine</h3>
      <p>Tout a commencé lors d'un retour du WES (Week-end ski) où Martin et Bernardo ont passé à peu près 20 minutes à trouver de la place dans le bus. L'organisation souvent "au
      dernier moment" du BDE n'aidant pas, il n'est jamais aisé de s'y retrouver dans le bus, notamment quand on veut rentrer à la CiuP. C'est alors que Bernardo a été frappé par un
      éclair de génie : on peut prévoir tout cela à l'avance ! C'est le rôle de Telecom Bus Planner.</p>

      <h3>L'équipe</h3>
      <p>L'équipe de développement est constituée à 100% d'élèves de l'école de tous horizons : de la Bretagne au Pays-Bas en passant par le Brésil ou encore les Haut de Seine il y en a pour tous les goûts ! </p>

